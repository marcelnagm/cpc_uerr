<p>A partir do dia 16 de Julho, algumas mudanças e melhorias se apresentaram na UERR.</p>

<p>O processo de matrícula se tornará mais interativo para você e irá minimizar a necessidade de
retificação e requerimentos após o período de matrícula.</p>

<p>Agora você irá solicita a sua matrícula pela internet.</p>

<p>Nosso objetivo é solicitar a Matrícula “on-line” nas disciplinas de Graduação nas diversas
Unidades da UERR, de forma geral, mais justa, coerente e eficiente.</p>

<p>A matrícula interativa “on-line” será efetivada de modo a intercalar períodos de matrícula ´on-
line´ com a consolidação das inscrições já efetuadas, onde, a partir de prioridades e critérios
previamente definidos, haverá a reserva ou não da vaga para o aluno.</p>

<p>Você esta recebendo seu usuário e uma senha, para que possa acessar o SISTEMA DE
MATRÍCULA no endereço eletrônico: http://www.eurr.edu.br .</p>

